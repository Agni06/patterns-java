/*
    * 
   * * 
  *   * 
 *     * 
* * * * * 
*/


public class Main
{
	public static void main(String[] args) {
	    int n=5;
	    
    for(int i=1;i<=n;i++)
        {
            for(int k =0 ;k<=(n-i-1);k++)
            {
                System.out.print(" ");
            }
            
           
            for(int j=1;j<=i;j++)
                {
                    if(i==j || j==1 || i==n )
                    {
                        System.out.print("* ");
                    }
                   else{
                       System.out.print("  ");
                   }
                 
                }
                System.out.println();
        }
    
    	}
}


